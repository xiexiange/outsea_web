export function getUi(locale) {
  return UI[locale] || UI.en;
}

export function localePath(locale, pathname = '/') {
  const loc = String(locale || 'en').trim() || 'en';
  const p = pathname.startsWith('/') ? pathname : `/${pathname}`;
  if (p === '/') return `/${loc}/`;
  return `/${loc}${p}`;
}
